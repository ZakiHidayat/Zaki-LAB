print("x <= y = ", x<=y)


# -------------- Operator Logika --------------

# and : mengembalikan True ketika kedua pernyataan benar
# or : mengembalikan True ketika salah satu pernyataannya benar
# not : menegasikan 

x = True
y = False

#Logika AND
a = x and y
print(a)

b = x or y
print(b)


