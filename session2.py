# -------------- Operator Aritmatika -------------- 

x = 9
y = 2
 
#penjumlahan
print("x + y = ", x+y)

#penguragan
print("x - y = ", x-y)

#perkalian
print("x x y = ", x*y)

#pembagian
print("x / y =", x//y)

#modulus
print("x % y = ", x%y)

#pangkat
print("x ** y = ", x**y)


# -------------- Operator Assignment -------------- 

